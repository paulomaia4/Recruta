    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed

        String niv = (String) cmbNivel.getSelectedItem();
        int codfilial = codfil;

        int nivel = Integer.parseInt(niv);

        ModeloLogin  ML = new ModeloLogin();
        ML.setUsuario(txtUsuario.getText());
        ML.setSenha(txtSenha.getText());
        ML.setId_nivel(nivel);
        ML.setFilial(codfilial);

        ControleLogin CL = new ControleLogin();
        CL.InsereLogin(ML);

        txtId.setText("");
        txtSenha.setText("");
        txtUsuario.setText("");
        cmbNivel.setSelectedItem(null);
        cmbFilial.setSelectedItem(null);

        preenchetabela();

    }//GEN-LAST:event_btnSalvarActionPerformed

