    public void setComboFilial() {

        int i = 0;
        cmbFilial.removeAllItems();
        while (i < listaFilial.size()) {
            cmbFilial.addItem(listaFilial.get(i).getApeFil());
            i++;
        }

    
    }
    
    
    public void preenchetabela(){
      
       List<ModeloLogin> Login = Recrutamento.Controle.ControleLogin.getInstancelogin().buscarLogin();

            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("Cod. Usuario");
            modelo.addColumn("Login");
            modelo.addColumn("Senha");
            modelo.addColumn("Nivel");
            modelo.addColumn("Filial");
            
            //modelo.addColumn("Situação");
          
        jtblUsuarios.setModel(modelo);
        jtblUsuarios.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        jtblUsuarios.getColumnModel().getColumn(0).setPreferredWidth(85);
        jtblUsuarios.getColumnModel().getColumn(1).setPreferredWidth(100);
        jtblUsuarios.getColumnModel().getColumn(2).setPreferredWidth(100);
        jtblUsuarios.getColumnModel().getColumn(3).setPreferredWidth(50);
        jtblUsuarios.getColumnModel().getColumn(4).setPreferredWidth(60);
        
        
        
      

            int y = 1;
            for (ModeloLogin i : Login) {
                modelo.addRow(new Object[]{i.getCodusu(),i.getUsuario(),i.getSenha(), i.getId_nivel(),i.getFilial() });
                //  jpb1.setValue(y);
                y++;
            }

            jtblUsuarios.removeAll();  // LIMPA O CONTEUDO DA TABELA

            jtblUsuarios.setModel(modelo);
    }
    
