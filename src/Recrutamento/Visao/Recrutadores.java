    public void preenchetabela(){
      
       List<ModeloRecrutadores> Recrutadores = Recrutamento.Controle.ControleRecrutadores.getInstancerecrutadores().buscarRecrutadores();

            DefaultTableModel modelo = new DefaultTableModel();

            modelo.addColumn("Código");
            modelo.addColumn("Nome");
            modelo.addColumn("Cargo");
            modelo.addColumn("Celular");
            
            
          
        jtblRecrutadores.setModel(modelo);
        jtblRecrutadores.setAutoResizeMode(jtblRecrutadores.AUTO_RESIZE_OFF);
        jtblRecrutadores.getColumnModel().getColumn(0).setPreferredWidth(50);
        jtblRecrutadores.getColumnModel().getColumn(1).setPreferredWidth(150);
        jtblRecrutadores.getColumnModel().getColumn(2).setPreferredWidth(100);
        jtblRecrutadores.getColumnModel().getColumn(3).setPreferredWidth(100);
        
      

            int y = 1;
            for (ModeloRecrutadores i : Recrutadores) {
                modelo.addRow(new Object[]{i.getCodrec(), i.getNomrec(), i.getCargo(), i.getNumcel()});
                //  jpb1.setValue(y);
                y++;
            }

            jtblRecrutadores.removeAll();  // LIMPA O CONTEUDO DA TABELA

            jtblRecrutadores.setModel(modelo);

       
          
      
      
      
      
      }
    
