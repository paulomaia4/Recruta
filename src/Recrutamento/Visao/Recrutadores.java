    private void BtnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnIncluirActionPerformed
       
        int codcargo = codcar;
                
        ModeloRecrutadores  PC = new ModeloRecrutadores();
        PC.setCodcar(codcargo);
        PC.setNomrec(txtNome.getText());
        PC.setNumcel(txtCel.getText());
        
        ControleRecrutadores CC = new ControleRecrutadores();
        CC.InsereRecrutadores(PC);
        
        
            txtCel.setText("");
            txtCod.setText("");
            txtNome.setText("");
            cmbCargo.setSelectedItem(null);
            
            preenchetabela();
        
    }//GEN-LAST:event_BtnIncluirActionPerformed

    private void jtblRecrutadoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtblRecrutadoresMouseClicked
        
        txtCel.setText("");
        txtCod.setText("");
        txtNome.setText("");
        cmbCargo.setSelectedItem(null);
        
        

                     
            int linhaSelecionada = jtblRecrutadores.getSelectedRow();

            txtCel.setText("" + jtblRecrutadores.getValueAt(linhaSelecionada, 3));
            txtCod.setText("" + jtblRecrutadores.getValueAt(linhaSelecionada, 0));
            txtNome.setText("" + jtblRecrutadores.getValueAt(linhaSelecionada, 1));
            
            
            //Encontrando a Posição dentro do List
            int i = 0;

            while (!listaCargo.get(i).getTitCar().equals(jtblRecrutadores.getValueAt(linhaSelecionada, 2))) {
                i++;
            }

            cmbCargo.setSelectedIndex(i);

                    
    }//GEN-LAST:event_jtblRecrutadoresMouseClicked

